package br.com.fabriciocs.erp.view.controller;

public class AlterarSenhaHelper {
	private String current;
	private String newPass;
	private String confirmNew;

	public AlterarSenhaHelper() {
	}

	public String getCurrent() {
		return current;
	}

	public void setCurrent(String current) {
		this.current = current;
	}

	public String getNewPass() {
		return newPass;
	}

	public void setNewPass(String newPass) {
		this.newPass = newPass;
	}

	public String getConfirmNew() {
		return confirmNew;
	}

	public void setConfirmNew(String confirmNew) {
		this.confirmNew = confirmNew;
	}

}